# dictionary ={key:'value'}
dict1 = {1: "python", 2: "java", 3: "javascript"}
print(dict1)
# Changing value
dict1[1] = "pythonv3"
print(dict1)
# Adding the value
dict1[4] = "kotlin"
print(dict1)
print(dict1.pop(1))
print(dict1)
# deleting the values in dictionary
del dict1[3]
print(dict1)
print(dict1.keys())
print(dict1.items())
